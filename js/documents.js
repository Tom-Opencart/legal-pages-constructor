    /**
     * Get document template by ID
     * @param {string} docId - Document identifier
     * @returns {Object} Document template with title and content
     */
    getTemplate(docId) {
        return this.templates[docId] || null;
    },
    
    /**
     * Generate document content with replacements
     * @param {string} docId - Document identifier
     * @param {Object} settings - Settings for replacements
     * @returns {string} Generated content
     */
    generateDocument(docId, settings) {
        const template = this.getTemplate(docId);
        if (!template) return '';
        
        let content = template.content;
        
        // Replace placeholders with actual values
        const replacements = {
            '[FULL_NAME]': settings.full_name || '[ПОЛНОЕ НАИМЕНОВАНИЕ]',
            '[INN]': settings.inn || '[ИНН]',
            '[OGRN]': settings.ogrn || '[ОГРН]',
            '[LEGAL_ADDRESS]': settings.legal_address || '[ЮРИДИЧЕСКИЙ АДРЕС]',
            '[ACTUAL_ADDRESS]': settings.actual_address || '[ФАКТИЧЕСКИЙ АДРЕС]',
            '[PHONE]': settings.phone || '[ТЕЛЕФОН]',
            '[EMAIL]': settings.email || '[EMAIL]',
            '[EMAIL_CONSENT]': settings.email_consent || '[EMAIL ДЛЯ СОГЛАСИЙ]',
            '[SITE_NAME]': settings.site_name || '[НАЗВАНИЕ САЙТА]',
            '[BANK_NAME]': settings.bank_name || '[НАИМЕНОВАНИЕ БАНКА]',
            '[BIK]': settings.bik || '[БИК]',
            '[CORR_ACCOUNT]': settings.corr_account || '[КОРР. СЧЁТ]',
            '[REG_ACCOUNT]': settings.reg_account || '[РАСЧ. СЧЁТ]',
            '[OWNER_TYPE]': settings.owner_type || '[ТИП СОБСТВЕННИКА]',
            '[DATE]': new Date().toLocaleDateString('ru-RU')
        };
        
        for (const [placeholder, value] of Object.entries(replacements)) {
            content = content.replace(new RegExp(placeholder.replace(/[[\]]/g, '\\$&'), 'g'), value);
        }
        
        return content;
    }
